void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int s;

	i = 0;
	while (i < size)
	{
		s = tab[i];
		tab[i] = tab[size - 1];
		tab[size - 1] = s;
		i++;
		size--;
	}
}
